var idade = 16

if (idade >= 18) {
    alert('Maior de idade')
} else if (idade == 15) {
    alert('Sua idade é 15')
} else {
    alert('Menor de idade')
}

var dia = 1
switch (dia) {
    case 1:
        console.log('Domingo')
        break
    case 2:
        console.log('Segunda-feira')
        break
    case 3:
        console.log('Terça-feira')
        break
    case 4:
        console.log('Quarta-feira')
        break
    case 5:
        console.log('Quinta-feira')
        break
    case 6:
        console.log('Sexta-feira')
        break
    case 7:
        console.log('Sábado')
        break
    default:
        console.log('Não é dia da semana')
}